namespace Hesaplama
{
    public static class Islem
    {
        public static decimal Topla(decimal a, decimal b)
        {
            return a + b;
        }

        public static decimal Cikarma(decimal a, decimal b)
        {
            return a - b;
        }

        public static decimal Carpma(decimal a, decimal b)
        {
            return a * b;
        }
        public static decimal Bolme(decimal a, decimal b)
        {
            return a / b;
        }
    }
}